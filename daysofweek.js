
let day=1;
switch(day) {
  case 1:
   console.log("sunday");
    break;
  case 2:
   console.log("monday");
    break;
  case 3:
   console.log("Tuesday");
    break;
  case 4:
   console.log("Wednesday");
    break;
  case 5:
   console.log("Thurday");
    break;
  case 6:
   console.log("Friday");
    break;
  case 7:
   console.log("Saturday");
    break;

  default:
    console.log("No Day with this number");
}
